class Solution
{
public:
    vector<int> singleNumber(vector<int> nums) 
    {
        // Code here.
        sort(nums.begin(),nums.end());
        int N=nums.size();
        vector<int> V;
        for(int i=0;i<N;i++){
            if(nums[i]==nums[i+1]){
                i++;
            }
            else{
                V.push_back(nums[i]);
            }
        }
        return V;
    }
};

